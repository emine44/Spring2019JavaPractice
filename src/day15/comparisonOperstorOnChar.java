package day15;

public class comparisonOperstorOnChar {

	public static void main(String[] args) {
		// comparison /relation operator
		
		char c1 ='a';
		char c2 = 'B';
		char c3 = ' ';
		// special character ---<number ----<lowercase----,uppercase
		
		System.out.println(c1==c2);
		System.out.println(c1!=c2);
		System.out.println(c1>=c2);
		System.out.println(c1<=c2);
		System.out.println(c1<c2);
		System.out.println(c3>c2);
	}

}
