package Fridays;

public class ManipulatingStrings {

	public static void main(String[] args) {
		//Concatenation:
		System.out.println(1+2); //if both operands are numeric + means numeric addition
		System.out.println("a" +"b"); //if either operand is a String + means concatenation
		System.out.println("a" + "b" + 3);// The expression is evaluated left to right
		System.out.println(1 + 2 + "c");

	}

}
