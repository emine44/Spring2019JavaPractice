package day6;

public class ComparisionOperator {

	public static void main(String[] args) {
		
     //if you have two number how to compare them
		int a =15, b = 11;
		//checking for equality ---> ==
		System.out.println("is a equals to b : " + (a==b));
		//checking for inequality ---> !=
		System.out.println(a!=b);
		//checking for greater or equal ---> ==
		System.out.println("is a and b greater or equal: " + (a>=b));
		//checking for greater or equal ---> >==
		System.out.println("is a and b greater or equal: " + (a>=b));
		boolean isAequalToB = (a == b);
		System.out.println( "canim" + isAequalToB);
	}

}
